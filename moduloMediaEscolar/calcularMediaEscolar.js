function calcularMedia(primeiraNota, segundaNota,terceiraNota, quartaNota ){
    let nota1 = primeiraNota
    let nota2 = segundaNota
    let nota3 = terceiraNota
    let nota4 = quartaNota

    mediaCalculada = ((nota1 + nota2 + nota3 + nota4) / 4)


}